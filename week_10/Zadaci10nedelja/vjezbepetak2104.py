def arrange_matrix(dim):
    matix = [[0 for j in range(dim)] for i in range(dim)]
    brojac = 1
    for i in range(dim):
        for j in range(dim):
          if i % 2 != 0:
              matix[dim - j - 1][i] = brojac
          else:
              matix[j][i] = brojac
          brojac += 1
    return matix

print(arrange_matrix(3))

