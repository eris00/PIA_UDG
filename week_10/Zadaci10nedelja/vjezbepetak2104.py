matrica = [[1, 2, 3], [4, 2, 7], [6, 8, 4]]

suma = 0
proizvod = 1

for i in range(len(matrica)):
  for j in range(len(matrica[i])):
    if matrica[i] == matrica[j]:
      suma += matrica[i][j]
    elif i + j == len(matrica) - 1:
      proizvod *= matrica[i][j]

if suma > proizvod:
  print("Suma je veca od prozvoda.")
else:
  print("Proizvod je veci od sume.")

